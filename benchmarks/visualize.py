"""Matplotlib visualisations for benchmark results.

Produces bar charts, histograms, heat-maps, and box-plots that
accompany the textual reports generated by ``reports.py``.
"""

import matplotlib.pyplot as plt
import numpy as np
from pathlib import Path
from typing import Dict, List

from benchmarks.runners.base import RunnerResult


def plot_accuracy_comparison(
    metrics: Dict,
    output_path: Path,
):
    """Bar chart of Top-1 vs Top-3 accuracy for every method."""
    methods = [m for m in metrics if m != "agreement"]
    top1 = [metrics[m].get("top1_accuracy", 0) for m in methods]
    top3 = [metrics[m].get("top3_accuracy", 0) for m in methods]

    x = np.arange(len(methods))
    width = 0.35

    fig, ax = plt.subplots(figsize=(10, 6))
    ax.bar(x - width / 2, top1, width, label="Top-1", color="#1f77b4")
    ax.bar(x + width / 2, top3, width, label="Top-3", color="#ff7f0e")
    ax.set_xlabel("Method")
    ax.set_ylabel("Accuracy")
    ax.set_title("Top-1 vs Top-3 Accuracy")
    ax.set_xticks(x)
    ax.set_xticklabels(methods, rotation=15, ha="right")
    ax.legend()
    ax.set_ylim(0, 1.1)
    plt.tight_layout()
    fig.savefig(output_path)
    plt.close(fig)


def plot_confidence_distribution(
    results: List[RunnerResult],
    ground_truth: List[str],
    output_path: Path,
    title: str = "Confidence Distribution",
):
    """Overlaid histogram of confidence scores for correct vs incorrect predictions.

    Useful for spotting over-confident wrong answers (red bars on the
    right-hand side) or under-confident correct answers.
    """
    correct_conf = []
    incorrect_conf = []
    for r, gt in zip(results, ground_truth):
        if r.coverage and r.predictions:
            if r.top_species == gt:
                correct_conf.append(r.top_confidence)
            else:
                incorrect_conf.append(r.top_confidence)

    fig, ax = plt.subplots(figsize=(8, 5))
    ax.hist(correct_conf, bins=20, alpha=0.6, label="Correct", color="green")
    ax.hist(incorrect_conf, bins=20, alpha=0.6, label="Incorrect", color="red")
    ax.set_xlabel("Confidence")
    ax.set_ylabel("Count")
    ax.set_title(title)
    ax.legend()
    plt.tight_layout()
    fig.savefig(output_path)
    plt.close(fig)


def plot_agreement_heatmap(
    agreement: Dict[str, Dict[str, float]],
    output_path: Path,
):
    """Colour heat-map showing pairwise top-1 agreement between methods."""
    methods = sorted(agreement.keys())
    matrix = np.array([[agreement[m1][m2] for m2 in methods] for m1 in methods])

    fig, ax = plt.subplots(figsize=(8, 6))
    im = ax.imshow(matrix, cmap="YlOrRd", vmin=0, vmax=1)
    ax.set_xticks(np.arange(len(methods)))
    ax.set_yticks(np.arange(len(methods)))
    ax.set_xticklabels(methods, rotation=45, ha="right")
    ax.set_yticklabels(methods)
    ax.set_title("Pairwise Method Agreement")

    for i in range(len(methods)):
        for j in range(len(methods)):
            ax.text(j, i, f"{matrix[i, j]:.2f}", ha="center", va="center", fontsize=8)

    fig.colorbar(im, ax=ax)
    plt.tight_layout()
    fig.savefig(output_path)
    plt.close(fig)


def plot_ood_analysis(
    id_results: List[RunnerResult],
    ood_results: List[RunnerResult],
    output_path: Path,
):
    """Side-by-side box-plot of confidence on ID vs OOD samples.

    A clear downward shift for OOD indicates the model can at least
    express lower confidence on unfamiliar inputs.
    """
    id_conf = [r.top_confidence for r in id_results if r.coverage]
    ood_conf = [r.top_confidence for r in ood_results if r.coverage]

    fig, ax = plt.subplots(figsize=(8, 5))
    ax.boxplot([id_conf, ood_conf], labels=["In-Distribution", "OOD"])
    ax.set_ylabel("Confidence")
    ax.set_title("Confidence: In-Distribution vs OOD")
    plt.tight_layout()
    fig.savefig(output_path)
    plt.close(fig)
